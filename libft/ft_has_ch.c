int		ft_has_ch(char const *s, char c)
{
	unsigned int i;

	if (!s || !c)
		return (0);
	i = 0;
	while (s[i])
	{
		if (s[i] != c && s[i] != 0)
			return (1);
		++i;
	}
	return (0);
}
